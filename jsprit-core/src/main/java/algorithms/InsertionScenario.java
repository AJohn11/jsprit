package algorithms;

class InsertionScenario {
	
	InsertionFacts iFacts;
	InsertionData iData;
	public InsertionScenario(InsertionFacts iFacts, InsertionData iData) {
		super();
		this.iFacts = iFacts;
		this.iData = iData;
	}
	/**
	 * @return the iFacts
	 */
	public InsertionFacts getiFacts() {
		return iFacts;
	}
	/**
	 * @return the iData
	 */
	public InsertionData getiData() {
		return iData;
	}
	
	

}
